#include "libft.h"

void	ft_striteri(char *s, void (*f) (unsigned int, char*))
{
	int		i;
	char	*ns;

	i = 0;
	ns = (char *)s;
	if (s)
	{
		while (ns[i])
		{
			(*f)(i, &ns[i]);
			i++;
		}
	}
}
